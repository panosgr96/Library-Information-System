    private void AddToDropDown(){
        ConnectDb con = new ConnectDb();
        String sql = "SELECT CATEGORYID, NAME FROM categories";
        try{
            ResultSet rs =  con.ExecuteQuery(sql);
            this.DRCategory.removeAllItems();
            while(rs.next()){
                this.DRCategory.addItem(new CoboBoxItem(rs.getString("CATEGORYID"), rs.getString("NAME")));
            }
            con.Close();
        }
        catch(DataException |SQLException  ex )
        {
            String mes = ex.getMessage()+"\n"+ex.getCause().getMessage(); 
            
            MsgBox.error(mes,ex.getMessage());
        }
    }
 
