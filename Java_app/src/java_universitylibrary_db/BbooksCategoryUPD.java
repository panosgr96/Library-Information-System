    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
       CoboBoxItem itm =(CoboBoxItem)this.DRCategory.getSelectedItem();
       if (this.getAction() == PopUpJpanel.FormActionType.Insert)
       {
         ConnectDb con = new ConnectDb();
          String sql ="INSERT INTO books_categories( ISBN, CATEGORYID) ";
                 sql+= "VALUES ("+this.getPKValue()+","+itm.getValue().trim()+")";
            try{
                con.UpdateQuery(sql);
                con.Close();
            }
            catch(DataException ex )
            {
                String mes = ex.getMessage()+"\n"+ex.getCause().getMessage(); 
                MsgBox.error(mes,ex.getMessage());
            }                 
        }
       if (this.getAction() == PopUpJpanel.FormActionType.Update)
       {
         ConnectDb con = new ConnectDb();
          String sql ="UPDATE books_categories SET CATEGORYID = "+ itm.getValue().trim()+" \n" +
                "WHERE ISBN = "+this.getPKValue() +" AND CATEGORYID=" +this.getCategoryID() ;
            try{
                con.UpdateQuery(sql);
                con.Close();
            }
            catch(DataException ex )
            {
                String mes = ex.getMessage()+"\n"+ex.getCause().getMessage(); 
                MsgBox.error(mes,ex.getMessage());
            }                 
        }
       if (this.getAction() == PopUpJpanel.FormActionType.Delete)
       {
         ConnectDb con = new ConnectDb();
          String sql ="DELETE FROM books_categories WHERE ISBN = "+this.getPKValue()+" AND CATEGORYID = " +this.getCategoryID();
            try{
                con.UpdateQuery(sql);
                con.Close();
            }
            catch(DataException ex )
            {
                String mes = ex.getMessage()+"\n"+ex.getCause().getMessage(); 
                MsgBox.error(mes,ex.getMessage());
            }                 
        }
        this.getPopupWin().actionPerformed(evt);
    }//GEN-LAST:event_SaveButtonActionPerformed

