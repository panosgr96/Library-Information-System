    private void formAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_formAncestorAdded
        this.TXTISBN.setText(this.getPKValue());
        ConnectDb con = new ConnectDb();
        String sql = "SELECT ISBN, CATEGORYID FROM books_categories WHERE ISBN = ? AND CATEGORYID =?";
        String[] parameter = new String[2];
        parameter[0] = this.getPKValue();
        parameter[1] =String.valueOf(this.getCategoryID());
        try{
            ResultSet rs =  con.ExecuteQuery(sql,parameter);
            while(rs.next()){
              for (int i=0;i <this.DRCategory.getItemCount(); i++)
              {
                 if (Integer.parseInt(((CoboBoxItem)this.DRCategory.getItemAt(i)).getValue()) == Integer.parseInt(rs.getString("CATEGORYID").toString()))
                 {
                    this.DRCategory.setSelectedIndex(i);
                     break;
                 }
              }
            }
            con.Close();
        }
        catch(DataException |SQLException  ex )
        {
            String mes = ex.getMessage()+"\n"+ex.getCause().getMessage(); 
            MsgBox.error(mes,ex.getMessage());
        }   
        if (this.getAction() == PopUpJpanel.FormActionType.Delete)
       {
          this.DRCategory.setEditable(false);
          this.DRCategory.setEnabled(false);
       }
    }//GEN-LAST:event_formAncestorAdded

